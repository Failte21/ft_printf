#include "libft.h"

void	ft_putnbr(int n)
{
	long int ln;
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	ln = n < 0 ? -((long)n) : (long)n;
	if (ln < 10)
		ft_putchar(ln + '0');
	else
	{
		ft_putnbr(ln / 10);
		ft_putchar((ln % 10) + '0');
	}
}
